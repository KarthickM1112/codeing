package StringMethods;

public class CountCharacters {
	public static void main(String[]args) {
		String vegitable="brinjal";
		char[] veg=vegitable.toCharArray();
		System.out.println(vegitable.length());
		System.out.print(vegitable.indexOf("i"));
		System.out.println(vegitable.charAt(1));
		System.out.println(veg[1]);
		
	}

}
